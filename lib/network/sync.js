//----------------------------------------------------------------------------------------------------------------------
// Handles pings from the client.
//
// @module sync.js
//----------------------------------------------------------------------------------------------------------------------

function SyncHandler(socket)
{
    socket.on('ping', this._handlePing.bind(this));
} // end SyncHandler

SyncHandler.prototype._handlePing = function(respond)
{
    respond();
}; // end _handlePing

//----------------------------------------------------------------------------------------------------------------------

module.exports = SyncHandler;

